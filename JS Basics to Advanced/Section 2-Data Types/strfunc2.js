// continuation in built string functions

//toUpperCase() - converts all the alphabetic characters in a string to uppercase
var exampleString = "This is a JavaScript tutorial"
console.log(exampleString.toUpperCase())

//toLowerCase() - converts all the alphabetic characters in a string to lowercase
console.log(exampleString.toLowerCase())

// concat() - merges two or more strings
var firstName = "Maxwell"
var lastName = "Ndegwa"
console.log(firstName.concat(lastName))

// concatenation can also be done using the '+' sign
console.log(firstName + lastName)
//adding of spaces in a merged string
console.log(firstName.concat( " ",lastName))


